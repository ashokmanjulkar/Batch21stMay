package demo_package;

public class While_Loop 
{
	public static void main(String[] args)
	{
		int a=100;
		while(a>2)
		{
			System.out.println(a);
			a=a/2;
		}
		System.out.println("------------------------------------------------------");
		
		int b=0;
		while(b<=50)
		{
			b=b+2;
			System.out.println(b);
		}
	}

}
